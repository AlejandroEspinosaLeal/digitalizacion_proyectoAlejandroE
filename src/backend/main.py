@app.post("/auth/register", status_code=status.HTTP_201_CREATED)
def register(user_data: User, db: Session = Depends(get_db)) -> dict:
    """
    Registers a new User generating Bcrypt hashes.
    
    Parameters:
        user_data (User): The user payload schema containing email and plain password.
        db (Session): Scalable database dependency injection context.
        
    Returns:
        dict: Confirmation entity harboring user creation string and integer unique ID.
        
    Raises:
        HTTPException (400): If the submitted email address is already bound to another profile.
        
    Example:
        >>> POST /auth/register {"email": "hello@world.com", "hashed_password": "123"}
        {"message": "User successfully created", "id": 1}
    """
    # Validate unique email
    existing = db.exec(select(User).where(User.email == user_data.email)).first()
    if existing:
        raise HTTPException(status_code=400, detail="Email is already registered")
    
    user_data.hashed_password = get_password_hash(user_data.hashed_password)
    db.add(user_data)
    db.commit()
    db.refresh(user_data)
    return {"message": "User successfully created", "id": user_data.id}

def send_verification_email(to_email: str, code: str, is_recovery: bool = False):
    if not settings.SMTP_USER or not settings.SMTP_PASSWORD:
        print(f"⚠️ SMTP not configured in .env. The code for {to_email} is: {code}")
        return
        
    now = datetime.now()
    date_str = now.strftime("%d-%m-%Y")
    
    if is_recovery:
        subject = f"Password reset {date_str}"
        title = "Password Recovery"
        message_body = "You requested a password reset. Use the following 6-digit code. If you didn't request this, ignore this security message."
    else:
        subject = "Verification Code - File Sorter"
        title = "Logging In"
        message_body = "A login attempt was detected. Use the following 6-digit code to securely access your account."

    template = templates.get_template("email_verification.html")
    html_content = template.render({"title": title, "message_body": message_body, "code": code})

    try:
        msg = EmailMessage()
        msg['Subject'] = subject
        msg['From'] = settings.SMTP_USER
        msg['To'] = to_email
        msg.set_content(f"Your code is: {code}. Please activate HTML view to see this message.")
        msg.add_alternative(html_content, subtype='html')

        server = smtplib.SMTP('smtp.gmail.com', 587)
        server.starttls()
        server.login(settings.SMTP_USER, settings.SMTP_PASSWORD)
        server.send_message(msg)
        server.quit()
        logger.info(f"Verification HTML email successfully sent to {to_email}")
    except Exception as e:
        logger.error(f"Error sending SMTP email to {to_email}: {e}")

@app.post("/auth/token")
def login(user_credentials: User, background_tasks: BackgroundTasks, db: Session = Depends(get_db)) -> dict:
    """
    Evaluates hashed authentication signatures and triggers a 2FA Verification via SMTP.
    
    Parameters:
        user_credentials (User): The user payload schema containing plain login attributes.
        background_tasks (BackgroundTasks): Native FastAPI asynchronous thread orchestrator for Email sending.
        db (Session): Scalable database dependency injection context.
        
    Returns:
        dict: Standardized API Object informing the front-end that MFA stands active.
        
    Raises:
        HTTPException (401): If plain credentials violate Bcrypt hashes checks.
        
    Example:
        >>> POST /auth/token {"email": "hello@world.com", "hashed_password": "123"}
        {"status": "pending_verification", "message": "Verification code sent to email"}
    """
    user = db.exec(select(User).where(User.email == user_credentials.email)).first()
    if not user:
        raise HTTPException(status_code=401, detail="Invalid credentials")
    
    if user.failed_login_attempts >= 3:
        code = str(random.randint(100000, 999999))
        user.verification_code = code
        db.commit()
        background_tasks.add_task(send_verification_email, user.email, code, True)
        return {"status": "requires_reset", "message": "You have exceeded login attempts. Check your email to recover your account."}
        
    if not verify_password(user_credentials.hashed_password, user.hashed_password):
        user.failed_login_attempts += 1
        db.commit()
        raise HTTPException(status_code=401, detail="Invalid credentials")
    
    code = str(random.randint(100000, 999999))
    user.verification_code = code
    user.failed_login_attempts = 0
    db.commit()
    background_tasks.add_task(send_verification_email, user.email, code, False)
    
    return {"status": "pending_verification", "message": "Verification code sent to email"}

class VerifyRequest(SQLModel):
    email: str
    code: str

@app.post("/auth/verify")
def verify_code(req: VerifyRequest, db: Session = Depends(get_db)):
    user = db.exec(select(User).where(User.email == req.email)).first()
    if not user or user.verification_code != req.code:
        raise HTTPException(status_code=401, detail="Invalid code")
    
    user.verification_code = None
    user.failed_login_attempts = 0
    db.commit()
    
    access_token = create_access_token(subject=user.email)
    return {"access_token": access_token, "token_type": "bearer"}

class ForgotPasswordRequest(SQLModel):
    email: str

@app.post("/auth/forgot-password")
def forgot_password(req: ForgotPasswordRequest, background_tasks: BackgroundTasks, db: Session = Depends(get_db)):
    user = db.exec(select(User).where(User.email == req.email)).first()
    if user:
        code = str(random.randint(100000, 999999))
        user.verification_code = code
        db.commit()
        background_tasks.add_task(send_verification_email, user.email, code, True)
    return {"message": "If the email is correct, we have sent a recovery code."}

class ResetPasswordRequest(SQLModel):
    email: str
    code: str
    new_password: str

@app.post("/auth/reset-password")
def reset_password(req: ResetPasswordRequest, db: Session = Depends(get_db)):
    user = db.exec(select(User).where(User.email == req.email)).first()
    if not user or user.verification_code != req.code:
        raise HTTPException(status_code=401, detail="Invalid or expired code.")
    
    user.hashed_password = get_password_hash(req.new_password)
    user.verification_code = None
    user.failed_login_attempts = 0
    db.commit()
    
    access_token = create_access_token(subject=user.email)
    return {"access_token": access_token, "token_type": "bearer"}

# 5. DEVICE SYNCHRONIZATION AND CUSTOM RULES
@app.get("/devices/sync/{device_id}", response_model=List[SortingRule])
def sync_rules(device_id: str, db: Session = Depends(get_db)):
    """The local Python agent polls this endpoint to fetch Organization Rules."""
    # First, update the device's online status
    device = db.get(Device, device_id)
    if not device:
        # Register a new device if unseen
        device = Device(id=device_id, name=f"PC-{device_id[:4]}", user_id=1) 
        db.add(device)
    
    device.is_online = True
    device.last_sync = datetime.utcnow()
    db.commit()
    
    # Return all active sorting rules assigned to this device
    rules = db.exec(select(SortingRule).where(SortingRule.device_id == device_id)).all()
    return rules

