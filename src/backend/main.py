@app.post("/devices/log/report")
def receive_log_report(req_data: LogReportRequest, request: Request, background_tasks: BackgroundTasks, db: Session = Depends(get_db)):
    auth_header = request.headers.get("Authorization")
    if not auth_header or not auth_header.startswith("Bearer "):
        raise HTTPException(status_code=401, detail="Unauthorized")
    
    token = auth_header.split(" ")[1]
    try:
        payload = jwt.decode(token, settings.SECRET_KEY, algorithms=[settings.ALGORITHM])
        email = payload.get("sub")
    except JWTError:
        raise HTTPException(status_code=401, detail="Invalid token")
        
    user = db.exec(select(User).where(User.email == email)).first()
    if not user:
        raise HTTPException(status_code=401, detail="User not found")

    background_tasks.add_task(send_report_email_task, user.email, req_data.device_id, req_data.folder, req_data.logs)
        
    return {"status": "success", "message": f"Report sent to {user.email}"}

# 6. WEBSOCKET: LIVE EVENT INGESTION
@app.websocket("/ws/events/{device_id}")
async def agent_socket(websocket: WebSocket, device_id: str, db: Session = Depends(get_db)):
    await manager.connect(device_id, websocket)
    try:
        while True:
            # Receive Real-Time JSON movement events from the local agent
            data = await websocket.receive_text()
            event_json = json.loads(data)
            
            # Persist event into the SQLite history log
            new_event = FileEvent(
                device_id=device_id,
                filename=event_json.get("filename"),
                source_path=event_json.get("source_path"),
                dest_path=event_json.get("dest_path"),
                timestamp=datetime.utcnow()
            )
            
            with Session(engine) as session:
                session.add(new_event)
                session.commit()

            # Broadcast to all connected web dashboard clients
            category = new_event.dest_path.replace("\\", "/").rstrip("/").split("/")[-1] if new_event.dest_path else "Other"
            await manager.broadcast_to_dashboard({
                "type": "file_event",
                "category": category,
                "filename": new_event.filename,
                "device_id": device_id,
                "timestamp": new_event.timestamp.isoformat() if new_event.timestamp else None,
            })

    except WebSocketDisconnect:
        manager.disconnect(device_id)
    except Exception as e:
        manager.disconnect(device_id)

