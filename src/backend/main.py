# 3. WEBSOCKET CONNECTION MANAGER (For Real-Time telemetry)
class ConnectionManager:
    def __init__(self):
        self.active_agents: dict[str, WebSocket] = {}

    async def connect(self, device_id: str, websocket: WebSocket):
        await websocket.accept()
        self.active_agents[device_id] = websocket
        print(f"📡 Agent connected: {device_id}")

    def disconnect(self, device_id: str):
        if device_id in self.active_agents:
            del self.active_agents[device_id]
            print(f"❌ Agent disconnected: {device_id}")

    async def broadcast_to_dashboard(self, message: dict):
        # Relay live file events to all connected web dashboard clients
        await stats_router.dashboard_manager.broadcast(message)

manager = ConnectionManager()

